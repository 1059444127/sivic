/*!
 * Returns true if the lookup table has fixed value->color representations.
 * Otherwise returns false.
 */
bool svkLookupTable::IsLUTFixed()
{
    bool isFixed = false;
    if( this->type == svkLookupTable::CNI_FIXED 
        || this->type == svkLookupTable::CBF_FIXED ) {
        isFixed = true;
    }
	return isFixed;
}

